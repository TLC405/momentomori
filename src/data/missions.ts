export const getMissionsByRealm = (realmId: string): Mission[] => {
  return missions.filter((m) => m.realmId === realmId);
};

export const getMissionById = (id: string): Mission | undefined => {
  return missions.find((m) => m.id === id);
};

export const getNewMissions = (): Mission[] => {
  return missions.filter((m) => m.isNew);
};

export const getHotMissions = (): Mission[] => {
  return missions.filter((m) => m.isHot);
};

export const getFeaturedMission = (): Mission => {
  // Rotate featured mission based on day of year
  const dayOfYear = Math.floor((Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86400000);
  const hotMissions = getHotMissions();
  return hotMissions[dayOfYear % hotMissions.length];
};
